package Java.ClassesObjectsPackages.TravelDetails;

public class Person {

    private String Name;
    private char Gender;
    private int age;
    
    public String getName() {
        return Name;
    }
    public void setName(String name) {
        Name = name;
    }
    public char getGender() {
        return Gender;
    }
    public void setGender(char gender) {
        Gender = gender;
    }
    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }
    
}
